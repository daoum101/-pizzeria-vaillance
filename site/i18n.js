function applyLang(lang) {
  const dict = TRANSLATIONS[lang];
  if (!dict) return;
  document.documentElement.lang = lang === 'nl' ? 'nl-BE' : (lang === 'en' ? 'en' : 'fr-BE');
  for (const sel in dict) {
    document.querySelectorAll(sel).forEach(el => { el.textContent = dict[sel]; });
  }
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  localStorage.setItem('vaillance_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('vaillance_lang') || (navigator.language || '').slice(0,2).toLowerCase();
  const lang = ['fr','nl','en'].includes(saved) ? saved : 'fr';
  if (lang !== 'fr') applyLang(lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });
});
